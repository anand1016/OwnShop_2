package com.anand.ownshop.bill;

public class DebitBill {
    private int debitBillId;
    private int amount;

    public int getDebitBillId() {
        return debitBillId;
    }

    public void setDebitBillId(int debitBillId) {
        this.debitBillId = debitBillId;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
